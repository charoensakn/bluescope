function getCurrentDateInThaiFormat() {
  const monthsThai = [
    'มกราคม',
    'กุมภาพันธ์',
    'มีนาคม',
    'เมษายน',
    'พฤษภาคม',
    'มิถุนายน',
    'กรกฎาคม',
    'สิงหาคม',
    'กันยายน',
    'ตุลาคม',
    'พฤศจิกายน',
    'ธันวาคม',
  ];
  const now = new Date();
  const day = now.getDate();
  const month = monthsThai[now.getMonth()];
  const year = now.getFullYear() + 543;
  return `${day} ${month} ${year}`;
}

